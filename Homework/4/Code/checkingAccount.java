public class checkingAccount {
	private int _noOfChecks;
	private float _transactions;
	private float _billPayments;

	public int getNoOfChecks() {
		return this._noOfChecks;
	}

	public void setNoOfChecks(int aNoOfChecks) {
		this._noOfChecks = aNoOfChecks;
	}

	public float getTransactions() {
		return this._transactions;
	}

	public void setTransactions(float aTransactions) {
		this._transactions = aTransactions;
	}

	public float getBillPayments() {
		return this._billPayments;
	}

	public void setBillPayments(float aBillPayments) {
		this._billPayments = aBillPayments;
	}
}